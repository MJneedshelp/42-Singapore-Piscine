#include <unistd.h>

int	ft_atoi(char *str)
{
	int	i;
	int	result;
	int	num_neg;

	i = 0;
	num_neg = 1;
	result = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			num_neg = num_neg * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + (str[i] - 48);
		i++;
	}
	return (num_neg * result);
}
